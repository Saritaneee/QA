public class Arithmatic {
    public static void main(String[] args) {
        int a = 9;
        int b = 4;

        //tambah
        int sum = a + b;
        System.out.println(sum);

        //kurang
        int difference = a - b;
        System.out.println(difference);

        //kali
        int product = a * b;
        System.out.println(product);

        //bagi
        int quotient = a / b;
        System.out.println(quotient);

        //modulus
        int remainder = a % b;
        System.out.println(remainder);

    }
}
